    private void jMenuItemCargarSisActionPerformed(java.awt.event.ActionEvent evt) {
        try {

            ObjectInputStream in = new ObjectInputStream(Files.newInputStream(Paths.get("datos.ser")));
            Sistema nuevoSis = (Sistema) in.readObject();
            this.modelo = nuevoSis;
            in.close();
             JOptionPane.showMessageDialog(this, "Se cargo el sistema ","Cargar", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException | ClassNotFoundException e) {
             JOptionPane.showMessageDialog(this, "Error al cargar el sistema ","Cargar", JOptionPane.ERROR_MESSAGE);

        }
        VentanaMenu vent = new VentanaMenu(modelo);
        vent.setVisible(true);
        dispose();
    }
    

